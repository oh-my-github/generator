export default class BasicCalculator {
  public constructor(protected value: number = 0) {}

  public currentValue(): number { return this.value; }

  public add(operand: number) { this.value += operand; return this; }
  public subtract(operand: number) { this.value -= operand; return this; }
  public multiply(operand: number) { this.value *= operand; return this; }
  public divide(operand: number) { this.value /= operand; return this; }

}